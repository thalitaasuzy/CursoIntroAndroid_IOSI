fun main() {
    var i = 0

    while(i<10){
        i++
        print("${i} ")
    }
    print("\n")
    print(i)
    print("\n")
    while(i>=1){
        i--
        print("${i} ")
    }
    print("\n")
    var str = "Curso Android"
    var j = 0
    while (j < str.length){
        print("${j} ")
        j++
    }
    print("\n")
    print("${str[0]}")
    print("\n")
    print("${str.length}")
}